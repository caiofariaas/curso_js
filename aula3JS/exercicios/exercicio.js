// Pedindo duas entradas para o usuário usando o prompt e retornando a soma dos mesmos! com Alert
// Lembrando que prompt armazena os valores como String, então, vamos fazer a conversão junto com a entrada!
// Também é possivel utilizar o 'Number'

let num1 = parseFloat(prompt('Digite um número'));
let num2 = parseFloat(prompt('Digite um número'));

let nume = Number(prompt("Entre com um número"));

alert(`A soma dos números digitados é ${num1 + num2 + nume}!`);
